function sonIguales(x, y) {
  // La función recibe dos argumentos "x" e "y".
  // Retorna true si "x" e "y" son iguales.
  // De lo contrario, retorna false.
  // Tu código:
  const var1 = x;
  const var2 = y;
  if (var1 === var2){
    return true;
  }
  return false;
}

module.exports = sonIguales;